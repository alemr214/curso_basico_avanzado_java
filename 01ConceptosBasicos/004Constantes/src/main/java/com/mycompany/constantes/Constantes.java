package com.mycompany.constantes;

/**
 *
 * @author alemr
 */
public class Constantes {

    public static void main(String[] args) {
        /*
            Para declarar constantes se le antece
            la palabra 'final' al tipo de dato
        */
        
        final int numero = 10;
        
        System.out.println(numero);
    }
    
}
